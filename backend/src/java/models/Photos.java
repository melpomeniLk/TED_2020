package models;
/**
 *
 * @author melpomeni_lk
 */
public class Photos {
    private long id;
    private String photo;

    public Photos() {
    }

    public Photos(long id, String photo) {
        this.id = id;
        this.photo = photo;
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getPhoto() {
        return photo;
    }

    public void setPhoto(String photo) {
        this.photo = photo;
    }

    
    
}
